    public List<ColorComponent> GetByColorDistance(float difficulty)
    {
        List<ColorComponent> results = new List<ColorComponent>();
        List<ColorComponent> potentialResults;
        ColorComponent startColor;

        if (difficulty > 950)
            difficulty = 950;

        double maxDistance = 500 - (difficulty / 2);
        double minDistance = 100 - (difficulty / 2);
        do
        {
            potentialResults = new List<ColorComponent>();
            startColor = this[_random.Next(Count)];

            foreach (var color in this)
            {
                if (potentialResults.Contains(color) || startColor == color)
                    continue;

                double distance = ColorDistance(startColor.Color, color.Color);

                if (distance >= minDistance && distance <= maxDistance)
                    potentialResults.Add(color);
            }
        } while (potentialResults.Count <= 16);

        results.Add(startColor);

        while (results.Count < 16)
        {
            int index = _random.Next(potentialResults.Count);
            var entry = potentialResults[index];
            if (!results.Contains(entry))
                results.Add(entry);
        }

        return results;
    }

    private double ColorDistance(Color color1, Color color2)
    {
        // euclidean distance in RGB color space
        return Math.Sqrt(Math.Pow(color1.R - color2.R, 2) + Math.Pow(color1.G - color2.G, 2) +
                         Math.Pow(color1.B - color2.B, 2));
    }
